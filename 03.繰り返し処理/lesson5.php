<?php
// 以下をfor文を使用して表示してください。
// ヒント: forの中でforを３回使用

// ********1
// *******121
// ******12321
// *****1234321
// ****123454321
// ***12345654321
// **1234567654321
// *123456787654321
// 12345678987654321


for ($j=1; $j<=9; $j++){
    for ($i=1; $i<=9-$j; $i++){
        echo '*';
    }
    for ($k=1; $k<=$j; $k++){
        echo $k;
    }
    $k=$k-2;
    for ($m=$k; $m>=1; $m--){
        echo $m;
    }
    echo '<br>';
}